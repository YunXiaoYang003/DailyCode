package com.itheima.type;

public class TypeDemo1 {
    public static void main(String[] args) {
        //目标：自动类型转换
        byte a=20;
        int b=a;
        System.out.println(a);
        System.out.println(b);

        int age=17;
        double db=age;
        System.out.println(db);

        char ch='a';
        int c=ch;
        System.out.println(c);

    }
}
